package com.kg.cinema.seat;

public class Seatbean {
	private int seat_id;
	private int seatstyle;
	private String seatgroup;
	private int seatno;
	private int seattype;
	private int left;
	private int top;
	
	public int getSeat_id() {
		return seat_id;
	}
	public void setSeat_id(int seat_id) {
		this.seat_id = seat_id;
	}
	public int getSeatstyle() {
		return seatstyle;
	}
	public void setSeatstyle(int seatstyle) {
		this.seatstyle = seatstyle;
	}
	public String getSeatgroup() {
		return seatgroup;
	}
	public void setSeatgroup(String seatgroup) {
		this.seatgroup = seatgroup;
	}
	public int getSeatno() {
		return seatno;
	}
	public void setSeatno(int seatno) {
		this.seatno = seatno;
	}
	public int getSeattype() {
		return seattype;
	}
	public void setSeattype(int seattype) {
		this.seattype = seattype;
	}
	public int getLeft() {
		return left;
	}
	public void setLeft(int left) {
		this.left = left;
	}
	public int getTop() {
		return top;
	}
	public void setTop(int top) {
		this.top = top;
	}
	
}
